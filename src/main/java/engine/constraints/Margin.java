package engine.constraints;

public class Margin {

    private int marginUp = 0;
    private int marginDown = 0;
    private int marginRight = 0;
    private int marginLeft = 0;

    public void setMarginUp(int marginUp) {
        this.marginUp = marginUp;
    }

    public void setMarginDown(int marginDown) {
        this.marginDown = marginDown;
    }

    public void setMarginRight(int marginRight) {
        this.marginRight = marginRight;
    }

    public void setMarginLeft(int marginLeft) {
        this.marginLeft = marginLeft;
    }

    public int getMarginUp() {
        return marginUp;
    }

    public int getMarginDown() {
        return marginDown;
    }

    public int getMarginRight() {
        return marginRight;
    }

    public int getMarginLeft() {
        return marginLeft;
    }

}
